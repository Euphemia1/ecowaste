// Pricing Calculator
document.addEventListener('DOMContentLoaded', function() {
    const wasteInput = document.getElementById('wasteAmount');
    const pickupsInput = document.getElementById('pickupsCount');
    
    function calculateImpact() {
        const waste = parseFloat(wasteInput.value) || 0;
        const pickups = parseInt(pickupsInput.value) || 0;
        
        // Calculate cost: ZMW 50 per pickup + ZMW 5 per kg
        const totalCost = (pickups * 50) + (waste * 5);
        
        // Calculate environmental impact (estimates)
        const co2Saved = (waste * 1.5).toFixed(0); // 1.5 kg CO2 per kg waste
        const treesSaved = Math.floor(waste / 25); // 1 tree per 25 kg
        const waterSaved = (waste * 5).toFixed(0); // 5 L per kg
        
        // Update display
        document.getElementById('totalCost').textContent = totalCost.toFixed(0);
        document.getElementById('co2Saved').textContent = co2Saved;
        document.getElementById('treesSaved').textContent = treesSaved;
        document.getElementById('waterSaved').textContent = waterSaved;
    }
    
    wasteInput.addEventListener('input', calculateImpact);
    pickupsInput.addEventListener('input', calculateImpact);
    
    // Initial calculation
    calculateImpact();
});
</script>
